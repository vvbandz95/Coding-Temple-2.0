# 1.1 Task 1: Create functions for each arithmetic operation.

# For addition
def add(x, y):
    return x + y

# For subtraction
def subtract(x, y):
    return x - y

# For multiplication
def multiply(x, y):
    return x * y

# For division
def divide(x, y):
    if y == 0:
        return "Error: Division by zero is not allowed."
    return x / y
